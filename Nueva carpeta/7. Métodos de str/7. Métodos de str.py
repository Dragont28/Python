variable = "Cereales"

variable.capitalize("") # Pone la primera letra en mayuscula

variable.isdigit("") # Te dice si es un número

variable.upper("") # Te pone todo en mayúscula

variable.lower("") # Te pone todo en minúscula

variable.swapcase("") # Cambia mayúsculas por minúsculas y viceversa

variable.replace("","") # Reemplaza caráctere

variable.find("l") # Indice de los carácteres, en este caso 6

variable.inder("") # Lo mismo que find pero lanza ValueError si no encuentra los carácteres

variable.isalpha("") # Te dice si son letras

variable.isspace("") # Te dice si son carácteres en blanco

variable.split("") # Te quita los espacios al final y al principio

variable.rfind("e") # Devuelve el ultimo carácter/s que coincide/n en este caso 7

variable.rindex("") # Da value error si no lo encuentra